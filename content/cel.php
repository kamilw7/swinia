<p class="p1"><b>Cel i zadania portalu</b></p>
<p class="p2"><br></p>
<p class="p3">Portal www.swinia.cc jest portalem skarg konsumenckich.</p>
<p class="p4"><br></p>
<p class="p3">Celem portalu jest ostrzeganie konsumentów przed aktami nieuczciwości korporacji oraz osób fizycznych zajmujących się obsługą klienta. Ponadto z godnie z art 22 zn 1 ustawy kodeks cywilny konsumentem jest osoba fizyczna w relacji z inną osobą fizyczną. Na portalu możliwe jest więc również ostrzeganie przed nieuczciwością osób fizycznych występujących w relacjach z konsumentem jako osoba fizyczna i nie jako przedsiębiorca.</p>
<p class="p4"><br></p>
<p class="p3">Zdaniem twórców portalu aktywność konsumentów dokonujących wpisów może oddziaływać prewencyjnie na ich adresatów oraz poprawiać standard usług. Ponadto funkcjonowanie portalu ma za zdanie wyrównać dostrzegalną słabość systemu ochrony konsumenta w naszym kraju.</p>
<p class="p4"><br></p>
<p class="p3">Każdy z nas nie raz był oszukany przez firmę ubezpieczeniową, bank, najemcę, ekspedientkę czy inne osoby. Wielu z nas doznało drobnych szkód co do których tzw. <b>„racjonalna apatia”</b> podpowiada nam, że nie ma ekonomicznego sensu składać pozwów sądowych czy informować o nich organów ścigania. Funkcjonowanie portalu ma za zadanie wyrównać de istotną wadę naszego systemu prawnego.</p>
<p class="p4"><br></p>
<p class="p3"><b>Wpisy konsumentów</b></p>
<p class="p4"><br></p>
<p class="p3">Wpisy na portalu dotyczą jedynie przypadków łamania praw konsumenckich oraz zasad współżycia społecznego związanych ochroną konsumentów. Inne wpisy jak również wpisy nieprawdziwe będą usuwane przez administratora portalu po poinformowaniu go o tym oraz po zweryfikowaniu prawdziwości informacji u autora wpisu. W przypadku gdy weryfikacja da wynik negatywny administrator ma prawo obciążyć wnioskodawcę jej kosztami.</p>
<p class="p4"><br></p>
<p class="p3">Wpisy jakich dokonują konsumenci mogą dotyczyć jedynie faktów i nie mogą zawierać wypowiedzi oceniających, czy naruszających normy prawa karnego bądź cywilnego. Wypowiedzią oceniającą nie jest wstępna kwalifikacja prawna czynu np. oszustwo, kradzież czy wyłudzenie. Przy czym kwalifikacja taka powinna być uzupełnioną słowem „podejrzenie” o ile osoba wpisująca nie dysponuje prawomocnym wyrokiem sądowym. Dopuszczalne są również opisy zachowania nie zawierające elementu ocennego, np. grubiaństwo, nieuprzejmość, chamstwo.</p>
<p class="p4"><br></p>
<p class="p3">Portal jest jedynie nośnikiem informacji konsumenckiej. Wpisujący się ponoszą pełną odpowiedzialność za naruszenie dóbr osobistych adresata wpisu jak również za naruszenia o charakterze karnoprawnym. W razie żądania skierowanego ze strony odpowiednich organów portal zastrzega możliwość udostępnienia wszystkich danych wpisującego będących w jego posiadaniu.</p>
<p class="p4"><br></p>
<p class="p3"><b>Zatarcie wpisu</b></p>
<p class="p4"><br></p>
<p class="p3">Istnieje możliwość zatarcia wpisu na portalu o ile wpisujący wyrazi na to zgodę lub odpadną jego podstawy. Portal oferuje również możliwość zorganizowania i przeprowadzenia postępowania mediacyjnego między wpisującym, a jego adresatem. Postępowanie mediacyjne jest przeprowadzane przez zewnętrzną kancelarię prawną i jest związane z kosztami. Cennik postępowania mediacyjnego jest udostępniany na żądanie osoby zainteresowanej postępowaniem mediacyjnym.</p>
<p class="p4"><br></p>
<p class="p3">Skierowanie zapytania do wpisującego o zgodę na zatarcie wpisu następuje jedynie na wniosek adresata. Nakład pracy administratora portalu związany z obsługa wniosku adresata jest rozliczany ryczałtowo i wynosi 25 zł za każdy wniosek. Opłata pokrywa koszt korespondencji do wnioskodawcy, jak i sprawdzenie okoliczności i prawdziwości danych.</p>
